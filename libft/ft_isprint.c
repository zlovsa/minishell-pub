#include "libft.h"

int	ft_isprint(int c)
{
	return (32 <= c && c <= 126);
}

int	ft_str_isspace(char *s)
{
	while (*s && (*s == ' ' || (9 <= *s && *s <= 13)))
		s++;
	return (!*s);
}
