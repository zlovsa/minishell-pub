#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0 && s[i] != c)
		i--;
	if (i >= 0 && s[i] == c)
		return ((char *)(s + i));
	return (NULL);
}
