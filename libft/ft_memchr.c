#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*src;
	size_t			i;

	src = (unsigned char *)s;
	i = 0;
	while (i < n && src[i] != (unsigned char)c)
		i++;
	if (i < n)
		return (src + i);
	return (NULL);
}
