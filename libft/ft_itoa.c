#include "libft.h"

char	*ft_itoa(int n)
{
	int		l;
	long	p;
	char	*r;

	l = n <= 0;
	p = n;
	while (p && ++l)
		p /= 10;
	r = (char *)malloc(l + 1);
	if (!r)
		return (NULL);
	r[l] = 0;
	r[0] = '0';
	p = n;
	if (n < 0)
	{
		r[0] = '-';
		p *= -1;
	}
	while (p)
	{
		r[--l] = p % 10 + '0';
		p /= 10;
	}
	return (r);
}
